// ─── Monster Ability Interface ───────────────────────────────
export interface MonsterAbility {
  id: string;
  name: string;
  description: string;
  type: AbilityType;
  damage_type: DamageType;
  target_type: TargetType;
  damage_formula: string;
  cooldown: number;
  range: number;
  cast_time: number;
  effects: string[];
}

// ─── Monster Template ────────────────────────────────────────
export interface MonsterTemplate {
  id: string;
  name: string;
  description: string;
  level: number;
  hp: number;
  stats: EntityStats;
  aggro_range: number;
  leash_range: number;
  respawn_time: number;
  xp_reward: number;
  loot_table: LootEntry[];
  abilities: MonsterAbility[];
  is_boss: boolean;
  boss_type: 'none' | 'mini_boss' | 'world_boss';
  family: string;
  zone_ids: string[];
}

// ─── Helper: base stats for a given level ────────────────────
function baseStats(
  level: number,
  overrides: Partial<EntityStats> = {},
): EntityStats {
  const s = level * 2;
  return {
    strength: s,
    agility: s,
    intellect: Math.floor(s * 0.5),
    spirit: Math.floor(s * 0.5),
    stamina: s,
    armor: level * 3,
    fire_resist: 0,
    ice_resist: 0,
    lightning_resist: 0,
    holy_resist: 0,
    shadow_resist: 0,
    nature_resist: 0,
    critical_chance: 5 + level * 0.3,
    critical_damage: 1.5,
    haste: 0,
    dodge: level * 0.2,
    block: 0,
    parry: 0,
    hit_chance: 90 + level * 0.5,
    spell_power: Math.floor(s * 0.3),
    attack_power: s,
    ...overrides,
  };
}

